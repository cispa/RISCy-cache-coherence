#!/usr/bin/env python3
import pandas as pd
from pathlib import Path

data_dir = Path("b2_ctg_performance_results")
SKIP_CPUS = {"C908", "X60", "Cortex-A53", "Cortex-A55", "Cortex-A520", "Cortex-A73", "Carmel", "C906"}

rows = []

for csv_file in sorted(data_dir.glob("*.csv")):
    cpu_name = csv_file.stem.replace("_", "-")
    if cpu_name in SKIP_CPUS:
        continue
    df = pd.read_csv(csv_file)
    row_archsc = df.iloc[1]
    assert(row_archsc["timer"] == 0)
    row_timer = df.iloc[0]
    assert(row_timer["timer"] == 1)
    cpu_display = cpu_name
    rows.append([
        cpu_display,
        f"{row_archsc['throughput']/1000:.2f}",
        f"{row_archsc['error-rate']:.2f}",
        f"{row_archsc['resolution']:.2f}",
        f"{row_timer['throughput']/1000:.2f}",
        f"{row_timer['error-rate']:.2f}",
        f"{row_timer['resolution']:.2f}"
    ])

latex = []
latex.append(r'% Generated by b2_ctg_performance/table.py')
latex.append(r"\begin{tabular}{lrrrrrrrrr}")
latex.append("  \\toprule")
latex.append(
    "  & \\multicolumn{3}{c}{\\textbf{Error Rate} (\\si{\\percent}, $\\downarrow$)}"
    " & \\multicolumn{3}{c}{\\textbf{Throughput} (\\si{\\kilo\\bit\\per\\second}, $\\uparrow$)}"
    " & \\multicolumn{3}{c}{\\textbf{Resolution} (\\si{\\micro\\second}, $\\downarrow$)} \\\\"
)
latex.append("  \\cmidrule(lr){2-4}\\cmidrule(lr){5-7}\\cmidrule(lr){8-10}")
latex.append(
    "  \\textbf{Microarch.}"
    " & {\\textbf{Timer}} & {\\textbf{\\bbone}} & {$\\Delta$}"
    " & {\\textbf{Timer}} & {\\textbf{\\bbone}} & {$\\Delta$\\%}"
    " & {\\textbf{Timer}} & {\\textbf{\\bbone}} & {$\\Delta$\\%} \\\\"
)
latex.append("  \\midrule")


def fmt_delta(fr_val_str: str, our_val_str: str, direction: str, abs_change=False) -> str:
    """
    Return a LaTeX-colored percentage delta string using \\good or \\bad.
    Delta is (Ours - F+R) / F+R * 100.
    direction: 'up' (higher better) or 'down' (lower better).
    """
    try:
        fr = float(fr_val_str)
        ours = float(our_val_str)
        if abs_change:
            delta = ours - fr
        else:
            if fr == 0.0:
                return r"???"
            delta = (ours - fr) / fr * 100.0
        val = f"{delta:+.2f}"
        good = (delta >= 0.0) if direction == "up" else (delta <= 0.0)
        colorcmd = r"\good" if good else r"\bad"
        return f"{colorcmd}{{{val}}}"
    except Exception:
        return r"???"

for cpu, thr_ours, err_ours, res_ours, thr_timer, err_timer, res_timer in rows:
    if cpu == "Cortex-A72":
        latex.append(
            f"  {cpu}\\tnote{{\\dag}} & "
            f"{err_timer} & - & - & "
            f"{thr_timer} & - & - & "
            f"{res_timer} & - & - \\\\"
        )
    else:
        d_thr = fmt_delta(thr_timer, thr_ours, direction="up")
        d_err = fmt_delta(err_timer, err_ours, direction="down", abs_change=True)
        d_res = fmt_delta(res_timer, res_ours, direction="down")
        latex.append(
            f"  {cpu} & "
            f"{err_timer} & {err_ours} & {d_err} & "
            f"{thr_timer} & {thr_ours} & {d_thr} & "
            f"{res_timer} & {res_ours} & {d_res} \\\\"
        )

latex.append("  \\bottomrule")
latex.append("\\end{tabular}")

latex_str = "\n".join(latex)

# Output to stdout
print(latex_str)

# Also write to file
with open("table.tex", "w", encoding="utf-8") as f:
    f.write(latex_str + "\n")
