#!/usr/bin/env python3
import pandas as pd
from pathlib import Path
from math import log
import sys

sys.path.append(str(Path(__file__).resolve().parents[1]))

from table_order import iter_grouped_rows

SKIP_CPUS = {"Cortex-A53", "Cortex-A55", "Carmel"}

data_dir = Path("timer_resolution_results")

def format_rel(val):
    cap = 2
    # cap = 5
    val = log(1+val)/log(1+cap)
    if val > 1:
        val = 1
    return r"\dotscale{0.5}{" + f"{val}" + "}"

rows = []

for csv_file in sorted(data_dir.glob("*.csv")):
    cpu_name = csv_file.stem.replace("_", "-")
    if cpu_name in SKIP_CPUS:
        continue
    df = pd.read_csv(csv_file)
    row = df.iloc[0]

    rows.append((
        cpu_name,
        f"{row['resolution']:.2f}",
        f"{row['minimal-increments']}",
        f"{row['flush-reload-diff']}",
        format_rel(row['relative-to-min-inc']),
        f"{row['i-flush-reload-diff']}",
        format_rel(row['i-relative-to-min-inc']),
    ))

latex = []
latex.append(r'% Generated by experiments/eval/timer_resolution/table.py')
latex.append(r'\begin{tabular}{lrrrcrc}')
latex.append(r'  \toprule')
latex.append(
    r'  \makecell{\textbf{Microarch.}} & '
    r'\makecell{\textbf{Resolution}\\\textbf{(\si{\nano\second})}} & '
    r'\makecell{\textbf{Min}\\\textbf{inc}} & '
    r'\makecell{\textbf{$\Delta$FR}\\\textbf{(\si{\nano\second})}} & '
    r'\makecell{\textbf{$\Delta$FR/\textit{min inc}}} & '
    r'\makecell{\textbf{$\Delta$iFR}\\\textbf{(\si{\nano\second})}} & '
    r'\makecell{\textbf{$\Delta$iFR/\textit{min inc}}} \\'
)
latex.append(r'  \midrule')
groups = iter_grouped_rows(rows, key=lambda row: row[0])
for group_idx, (_, group_rows) in enumerate(groups):
    if group_idx > 0:
        latex.append(r'  \midrule')
    for row in group_rows:
        latex.append("  " + " & ".join(row) + r" \\")
latex.append(r'  \bottomrule')
latex.append(r'\end{tabular}')

latex_str = "\n".join(latex)

# Output to stdout
print(latex_str)

# Also write to file
with open("table.tex", "w", encoding="utf-8") as f:
    f.write(latex_str + "\n")
