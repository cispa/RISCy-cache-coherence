#!/usr/bin/env python3

import argparse
import sys
from pathlib import Path

import pandas as pd

sys.path.append(str(Path(__file__).resolve().parents[1]))

from table_order import iter_grouped_rows

parser = argparse.ArgumentParser()
parser.add_argument("--compare-fr", action="store_true", help="Enable FR comparison")
args = parser.parse_args()

data_dir = Path("i2sc_dcache_covert_channel_performance_results")
SKIP_CPUS = {"C908", "X60", "Cortex-A53", "Cortex-A55", "Cortex-A520", "Cortex-A73", "Carmel", "C906", "Cortex-A72", "test-A72"}

COMPARE_FR_CPUS = ["Oryon", "C910", "3A5000-HV"]

rows = []

for csv_file in sorted(data_dir.glob("*.csv")):
    cpu_name = csv_file.stem.replace("_", "-")

    if not args.compare_fr:
        if cpu_name in SKIP_CPUS:
            continue
    else:
        if cpu_name not in COMPARE_FR_CPUS:
            continue

    df = pd.read_csv(csv_file)
    row = df.iloc[1]
    assert row["arch"] == 1, f"{csv_file}: expected arch==1 on row 1"
    frrow = df.iloc[0]
    assert frrow["arch"] == 0, f"{csv_file}: expected arch==0 on row 0"

    if args.compare_fr:
        rows.append((
            cpu_name,
            f"{row['throughput']/1000:.2f}",
            f"{row['error-rate']:.2f}",
            f"{row['capacity']/1000:.2f}",
            f"{row['resolution']:.2f}",
            f"{frrow['throughput']/1000:.2f}",
            f"{frrow['error-rate']:.2f}",
            f"{frrow['capacity']/1000:.2f}",
            f"{frrow['resolution']:.2f}",
        ))
    else:
        rows.append((
            cpu_name,
            f"{row['throughput']/1000:.2f}",
            f"{row['error-rate']:.2f}",
            f"{row['capacity']/1000:.2f}",
            f"{row['resolution']:.2f}",
        ))

def fmt_delta(fr_val_str: str, our_val_str: str, direction: str, abs_change=False) -> str:
    try:
        fr = float(fr_val_str)
        ours = float(our_val_str)
        if abs_change:
            delta = ours - fr
        else:
            if fr == 0.0:
                return r"???"
            delta = (ours - fr) / fr * 100.0
        val = f"{delta:+.1f}"
        good = (delta >= 0.0) if direction == "up" else (delta <= 0.0)
        colorcmd = r"\good" if good else r"\bad"
        return f"{colorcmd}{{{val}}}"
    except Exception:
        return r"???"


def ordered_compare_rows():
    rank = {cpu: idx for idx, cpu in enumerate(COMPARE_FR_CPUS)}
    return sorted(rows, key=lambda row: rank.get(row[0], len(COMPARE_FR_CPUS)))

latex = []
latex.append(r"% Generated by i2sc_dcache_covert_channel_performance/table.py")
if args.compare_fr:
    latex.append(r"\begin{tabular}{lrrrrrrrrrr}")
    latex.append("  \\toprule")
    latex.append(
        "  & \\multicolumn{3}{c}{\\textbf{Capacity} (\\si{\\kilo\\bit\\per\\second}, $\\uparrow$)}"
        " & \\multicolumn{3}{c}{\\textbf{Error Rate} (\\si{\\percent}, $\\downarrow$)}"
        " & \\multicolumn{3}{c}{\\textbf{Throughput} (\\si{\\kilo\\bit\\per\\second}, $\\uparrow$)} \\\\"
    )
    latex.append("  \\cmidrule(lr){2-4}\\cmidrule(lr){5-7}\\cmidrule(lr){8-10}")
    latex.append(
        "  \\textbf{Microarch.}"
        " & {\\textbf{F+R}} & {\\textbf{Ours}} & {$\\Delta$\\%}"
        " & {\\textbf{F+R}} & {\\textbf{Ours}} & {$\\Delta$}"
        " & {\\textbf{F+R}} & {\\textbf{Ours}} & {$\\Delta$\\%} \\\\"
    )
    latex.append("  \\midrule")
    for cpu, thr, err, cap, res, fr_thr, fr_err, fr_cap, fr_res in ordered_compare_rows():
        d_thr = fmt_delta(fr_thr, thr, direction="up")
        d_err = fmt_delta(fr_err, err, direction="down", abs_change=True)
        d_cap = fmt_delta(fr_cap, cap, direction="up")
        latex.append(
            f"  {cpu} & "
            f"{fr_cap} & {cap} & {d_cap} & "
            f"{fr_err} & {err} & {d_err} & "
            f"{fr_thr} & {thr} & {d_thr}\\\\"
        )
    latex.append("  \\bottomrule")
    latex.append("\\end{tabular}")
else:
    latex.append("\\begin{tabular}{lrrrr}")
    latex.append("  \\toprule")
    latex.append("  \\makecell{\\textbf{Microarch.}} & "
                 "\\makecell{\\textbf{Capacity}\\\\\\textbf{(\\si{\\kilo\\bit\\per\\second}, $\\uparrow$)}} & "
                 "\\makecell{\\textbf{Error Rate}\\\\\\textbf{(\\si{\\percent}, $\\downarrow$)}} & "
                 "\\makecell{\\textbf{Throughput}\\\\\\textbf{(\\si{\\kilo\\bit\\per\\second}, $\\uparrow$)}} & "
                 "\\makecell{\\textbf{Resolution}\\\\\\textbf{(\\si{\\micro\\second}, $\\downarrow$)}} \\\\"
                 )
    latex.append("  \\midrule")
    for group_idx, (_, group_rows) in enumerate(iter_grouped_rows(rows, key=lambda row: row[0])):
        if group_idx > 0:
            latex.append("  \\midrule")
        for cpu, throughput, err, cap, res in group_rows:
            latex.append(f"  {cpu} & {cap} & {err} & {throughput} & {res}\\\\")
    latex.append("  \\bottomrule")
    latex.append("\\end{tabular}")

latex_str = "\n".join(latex)

print(latex_str)

with open("table.tex", "w", encoding="utf-8") as f:
    f.write(latex_str + "\n")
